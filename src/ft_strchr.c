#include "../includes/push_swap.h"

int	fft_strchr(int *s, int c, int j, t_data *data)
{
	int	i;

	i = j + 1;
	while (i < data->j)
	{
		if (s[i] == c)
			return (0);
		i++;
	}
	return (1);
}
